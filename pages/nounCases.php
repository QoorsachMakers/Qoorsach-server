    <h1>
        Падежи существительных в английском языке
    </h1>
    <article>
        <b>Падеж</b> – это грамматическая категория, которая отражает взаимосвязь существительного с другими словами
        предложения.<br>
        <br>
        В древнеанглийском языке присутствовали именительный, винительный, родительный, творительный и дательный падежи.
        Со временем они отмирали и теперь в современном английском языке существует только два падежа – общий и
        притяжательный.<br>
        <br>
        <b>Общий падеж</b> (common case) английских существительных ничем не обозначен, то есть существительные в этом
        падеже имеют нулевое окончание (chair, car). Его значение очень размыто, и в зависимости от контекста
        существительное в общем падеже может выполнять самые разные функции.<br>
        <br>
        <b>Притяжательный падеж</b> (possessive/genitive case) чаще всего выражает принадлежность (отсюда название
        possessive).<br>
        <br>
        Притяжательный падеж образуется при помощи знака апострофа и буквы s (‘s) или просто апострофа (‘). Последний
        способ используется для существительных во множественном числе (pupils’ work, cars’ colour) и греческих
        заимствований, оканчивающихся на [ -iz ] (Xerxes’ army, Socrates’ wife).<br>
        <br>
        Однако если форма множественного числа образована не по обычному правилу (men, children), то в притяжательном
        падеже к ней прибавляется ‘s:<br>
        <blockquote>
            <span>
                men'<b>s</b> work, children'<b>s</b> toys
            </span>
        </blockquote>
        <br>
        Если существительное – составное, то ‘s прибавляется к последнему его элементу:<br>
        <blockquote>
            <span>
                 mother-in-law’<b>s</b> advice, passer-by’<b>s</b> surprise
            </span>
        </blockquote>
        <br>
        Иногда ‘s может относиться к нескольким существительным или целой фразе:<br>
        <blockquote>
            <span>
                <b> Peter and Sally’s</b> kids; <b>the girl I helped yesterday’s</b> face.<br>
                 Дети Питера и Сэлли; лицо девушки, которой я вчера помог.
            </span>
        </blockquote>
        <br>
        Нужно учесть, что притяжательный падеж одушевленных существительных в английском языке строится при помощи
        окончания ‘s, а для неодушевленных – при помощи предлога of:<br>
        <blockquote>
            <span>
                Kevin'<b>s</b> hat, income <b>of</b> the company<br>
                 шляпа Кевина, доход компании.
            </span>
        </blockquote>
        <br>
        Однако среди неодушевленных существительных есть исключения, которые можно поставить в форму притяжательного
        падежа через ‘s:<br>
        <ul>
            <li>
                Существительные, обозначающие время и расстояние: <span>today<b>’s</b> newspaper, a mile<b>’s</b> distance</span>
            </li>
            <li>
                Названия стран и городов: <span>Germany<b>’s</b> industry, New York<b>’s</b> streets</span>
            </li>
            <li>
                Названия газет и организаций: <span>the Guardian<b>’s</b> article, Red Cross<b>’s</b> volunteers</span>
            </li>
            <li>
                Слова <b>nation</b>, <b>country</b>, <b>city</b>, <b>town</b>:<span> country<b>’s</b> treasures</span>
            </li>
            <li>
                Слова <b>ship</b>, <b>car</b>, <b>boat</b>:<span
                > ship<b>’s</b> name, car<b>’s</b> speed</span></li>
            <li>
                Слова <b>nature</b>, <b>water</b>, <b>ocean</b>: <span>ocean<b>’s</b> temperature, nature<b>’s</b> beauty</span>
            </li>
            <li>
                Названия месяцев и времени года: <span>January<b>’s</b> frosts, summer<b>’s</b> days</span>
            </li>
            <li>
                Названия планет: <span>Saturn<b>’s</b> rings</span></li>
            <li>
                Некоторые устойчивые выражения: <span>at death<b>’s</b> door, at arm<b>’s</b> length, a hair<b>’s</b> breadth, at a snail<b>’s</b> pace</span>
                и другие.
            </li>
        </ul>
    </article>
    <h2>
        Выражение падежей английских существительных при помощи предлогов
    </h2>
    <article>
        Хотя в английском языке и насчитывается только два падежа, при помощи некоторых предлогов можно передать
        значение и других падежей.<br>
        <br>
        <b>Родительный падеж </b>передается при помощи предлога of:<br>
        <blockquote>
            <span>
                 The beginning <b>of the summer </b>was cold.<br>
                 Начало (чего?) лета было холодным.<br>
                <br>
                 The behavior of this new breed of mice is rather unusual.<br>
                 Поведение (кого?) этой новой породы мышей довольно необычно.<br>
            </span>
        </blockquote>
        <br>
        <b>Дательный падеж </b>соответствует предлогу to:<br>
        <blockquote>
            <span>
                 I am going <b>to John</b>.<br>
                 Я иду к (кому?) Джону.<br>
                <br>
                 We sold our house <b>to a family </b>from Canada.<br>
                 Мы продали свой дом (кому?) семье из Канады.
            </span>
        </blockquote>
        <br>
        <b>Творительный падеж </b>соответствует предлогу with, когда имеется в виду инструмент или предмет, которым
        выполняется действие:<br>
        <blockquote>
            <span>
                 I’ve killed two flies <b>with a newspaper.</b><br>
                 Я убил (чем?) газетой двух мух.<br>
                <br>
                 My sister can write <b>with</b> both her <b>hands</b>.<br>
                 Моя сестра умеет писать (чем?) обеими руками.
            </span>
        </blockquote>
        <br>
        Если в виду имеется действующее лицо или сила, то используется предлог by:<br>
        <blockquote>
            <span>
                 This machine can be operated only <b>by a professional</b>.<br>
                 Это устройство может управляться только (кем?) профессионалом.<br>
                <br>
                 We suddenly saw a hat carried <b>by the wind</b>.<br>
                 Вдруг мы увидели шляпку, несомую (чем?) ветром.
            </span>
        </blockquote>
        <br>
