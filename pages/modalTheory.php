    <h1>Модальные глаголы</h1>
    <article>
        Модальные глаголы в английском языке отличаются от остальных глаголов тем, что они не используются
        самостоятельно и не обозначают конкретного действия или состояния, они отражают его модальность, то есть
        отношение к нему говорящего. Вместе модальный глагол и инфинитив значащего глагола образуют составное модальное
        сказуемое (compound modal predicate):<br>
        <blockquote>
            <span>
                I <u>can play</u> volleyball<br>
                Я умею играть в волейбол
            </span>
        </blockquote>
        <br>
        О каком именно отношении идет речь? Например, говорящий может оценивать действие как возможное, необходимое,
        разрешаемое, просимое, запрещенное, приказываемое, маловероятное, очень вероятное и т. д.:<br>
        <blockquote>
            <span>
                Я поеду в Лондон.<br>
Я могу поехать в Лондон.<br>
Я должен поехать в Лондон.<br>
Можно я поеду в Лондон?
            </span>
        </blockquote>
        <br>
        В зависимости от такой оценки и структуры предложения нужно использовать один из следующих английских модальных
        глаголов.<br>
        <h2>
            Модальные глаголы в английском языке:
