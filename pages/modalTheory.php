            </li>
        </ul>
        Чаще всего используются первые три: Can, May и Must. Эти глаголы имеют самое общее значени и иногда могут
        заменять собой остальные модальные глаголы.<br>
        <br>
        <b>Вопросительные предложения</b> с модальными глаголами образуются без вспомогательного глагола to do, при этом
        модальный глагол выносится в начало предложения:<br>
        <blockquote>
            <span>
                Shall i help you?<br>
                Мне помочь?<br><br>
                Could you give me his address?<br>
                Не дадите мне его адрес?
            </span>
        </blockquote>
        <br>
        <b>Отрицательная форма</b> модального глагола образуется постановкой после него частицы not. Зачастую, особенно
        в устной речи, они сливаются в сокращенную форму:<br>
