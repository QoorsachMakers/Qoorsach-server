    <h1>Отсутствие артикля (нулевой артикль)</h1>
    <article>
        Отсутствие артикля перед существительным может быть таким же значимым, как и его наличие. В таком случае его
        могут называть нулевым артиклем (zero article).<br>
        <br>
        Артикль не нужно использовать в следующих случаях:
        <ul>
            <li>
                Там, где должен стоять неопределенный артикль, но это невозможно из-за множественного числа
                существительного:<br>
                <blockquote>
                    <span>
                        I see a man waving. I see people waving.<br>
                        Я вижу машущего человека. Я вижу машущих людей.
                    </span>
                </blockquote>
            </li>
            <li>
                С именами собственными, которые по своему значению являются уникальными и поэтому не нуждаются в
                уточнении:<br>
                <blockquote>
                    <span>
                        I hear Lucky scratching the door.<br>
                        Я слышу, как Лаки скребет дверь.
                    </span>
                </blockquote>
                <br>
            </li>
            <li>
                Если к существительному относятся другие определители (притяжательные, указательные, вопросительные и
                неопределенные местоимения, существительные в притяжательном падеже):<br>
                <blockquote>
                    <span>
                        I love her / these / Grandma’s stories.<br>
                        Я люблю ее / эти / бабушкины истории
                    </span>
                </blockquote>
            </li>
            <li>
                С неисчисляемыми абстрактными существительными:<br>
                <blockquote>
                    <span>
                        They left the room with dignity.<br>
                        Они с достоинством покинули комнату.<br>
                        <br>
                        She is an expert in modern art.<br>
                        Она – эксперт в современном искусстве
                    </span>
                </blockquote>
            </li>
            <li>
                С неисчисляемыми существительными, обозначающими вещество:<br>
                <blockquote>
                    <span>
                        Stone was the main material used in fortifications.<br>
                        Камень служил основным материалом при постройке укреплений.<br>
                        <br>
                        Water is a vital substance for our life.<br>
                        Вода – это крайне важное вещество в нашей жизни.
                    </span>
                </blockquote>
            </li>
            <li>
                С некоторыми географическими названиями:<br>
                <blockquote>
                    <span>
                        заливов: Cardiff Bay<br>
                        полуостровов: Kamchatka, Taymyr<br>
                        озер, если перед ними стоит слово «lake»: Lake Ontario<br>
                        водопадов: Niagara Falls<br>
                        островов: Malta<br>
                        отдельных гор (в отличие от цепей): Everest<br>
                        континентов: Europe, Asia<br>
                        стран: Croatia, Mexico<br>
                        городов: London, Saint-Petersburg
                    </span>
                </blockquote>
            </li>
            <li>
                Когда идет речь о двух тесно связанных предметах:<br>
                <blockquote>
                    <span>
                        They look like mother and son.<br>
                        Они похожи на мать и сына.<br>
                        <br>
                        They always quarrel like cat and dog.<br>
                        Вечно они ссорятся, как кошка с собакой.
                    </span>
                </blockquote>
            </li>
            <li>
                Вместе с указанием на единственный в данной ситуации пост или должность:<br>
                <blockquote>
                    <span>
                        Abraham Lincoln was elected President in 1861.<br>
                        Авраам Линкольн был избран президентом в 1861 году.<br>
                        <br>
                        You deserve to be chairman of the meeting.<br>
                        Вы заслуживаете того, чтобы быть председателем собрания.
                    </span>
                </blockquote>
            </li>
            <li>
                Вместе с титулами, обращениями и званиями, если за ними следует фамилия:<br>
                <blockquote>
                    <span>
                        Mister Jefferson, Minister Brown<br>
                        Мистер Джефферсон, министр Браун.
                    </span>
                </blockquote>
            </li>
            <li>
                Если значение существительного выражает скорее качество, а не предмет:<br>
                <blockquote>
                    <span>
                        Idiot I was to invite him to the party.<br>
                        Я был таким дураком, что пригласил его на вечеринку.<br>
                        <br>
                        You are not even man enough to admit your fault.<br>
                        У тебя даже мужества не хватает признать свою вину.
                    </span>
                </blockquote>
            </li>
        </ul>
