    <h2>
        Опущение артиклей в английском языке
    </h2>
    <article>
        В некоторых случаях артикль в английском языке бывает опущен, хотя в предложении предполагается его наличие. Это
        встречается в тех случаях, когда необходимо максимально сократить текст:<br>
        <ul>
            <li>
                В газетных заголовках:<br>
                <blockquote>
                    <span>
                        Old lady detains a robber.<br>
                        Старушка задерживает грабителя.<br>
                        <br>
                        Seven people witness UFO.<br>
                        Семь людей видели НЛО.
                    </span>
                </blockquote>
            </li>
            <li>
                В телеграммах:<br>
                <blockquote>
                    <span>
                        Monthly report needed send first opportunity.<br>
                        Требуется ежемесячный отчет отправьте первой возможности.
                    </span>
                </blockquote>
            </li>
            <li>
                В сценариях:<br>
                <blockquote>
                    <span>Actors stand near cupboard, then move to window.<br>
                    Актеры стоят у буфета, затем переходят к окну.</span>
                </blockquote>
            </li>
            <li>
                В сносках, примечаниях, словарных статьях:<br>
                <blockquote>
                    <span>
                        See Table 2 on page 24.<br>
                        Обратитесь к Таблице 2 на странице 24.<br>
    <br>
                        John Updike. American poet, dramatist, author of the novel “Rabbit, run.”<br>
                        Джон Апдайк. Американский поэт, драматург, автор романа «Кролик, беги».<br>
                        </span>
                </blockquote>
            </li>
        </ul>
