    <article>
        В отличие от русского языка, род английских существительных нельзя определить по окончанию слова. Часто его
        можно выявить только по лексическому значению или по контексту.<br>
        <br>
        <b>Мужской род</b> (mmasculine) - названия одушевленных существ мужского пола:<br>
        <blockquote>
            <span>
                man, boy, bull, uncle, bachelor, ox
            </span>
        </blockquote>
        <br>
        <b>Женский род</b> (feminine) - названия одушевленных существ женского пола:<br>
        <blockquote>
            <span>
                woman, girl, aunt, bride, hen
            </span>
        </blockquote>
        <br>
        <b>Средний род</b> (neuter) - названия неодушевленных предметов и животных неизвестного пола:<br>
        <blockquote>
            <span>
                television, lamp, sparrow
            </span>
        </blockquote>
        <br>
        Лишь иногда женский род можно определить по нескольким суффиксам:<br>
        <blockquote>
            <span>
                <b>-ine</b>: heroine - героиня<br>
                <b>ette</b>: usherette - билетерша<br>
                <b>-ess</b>: tighress, hostess - тигрица, хозяйка<br>
            </span>
        </blockquote>
        <br>
        Также иногда род можно указать, прибавив к существительному такие слова, как he/she, hen/cock, male/female,
        dog/bitch, boy/girl:<br>
        <blockquote>
            <span>
                woman-writer, she-wolf, hen-pheasant<br>
                писательница, волчица, самка фазана
            </span>
        </blockquote>
        <br>
        Некоторые неодушевленные существительные традиционно ассоциируются с определенным родом. Такое явление чаще
        всего встречается в художественной литературе и поэзии.<br>
        <b>Мужской род</b>: Sun, wind, fear, love.<br>
        <b>Женский род</b>: Earth, Moon, ship, boat, car, hope, justice, modesty, peace.<br>
        <blockquote>
            <span>
                Greeks worshipped the Sun moving across the sky in his golden chariot.<br>
                Греки почитали Солнце, движущееся по небу в своей золотой повозке.<br>
                <br>
                Dr. Patrick has bought a yacht and called her “The Spirit”.<br>
                Доктор Патрик купил яхту и назвал ее «The Spirit».<br>
            </span>
        </blockquote>
        <br>
        Кроме этого, к женскому роду относятся названия некоторых стран:<br>
        <blockquote>
            <span>
                I grew up in <b>England</b>. <b>She</b> will always remain in my memory.<br>
                Я вырос в Англии. Она навсегда останется в моей памяти.<br>
            </span>
        </blockquote>
        <br>
        Исключениями являются названия стран с нарицательной основой, например:<br>
        <blockquote>
            <span>
                 the <b>Union</b> of Soviet Socialist Republics, the United <b>States</b> of America.
            </span>
        </blockquote>
