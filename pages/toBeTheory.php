        В данных примерах <b>глагол to be</b> является самостоятельным глаголом. Так же, как и в русском языке, глагол
        to be может быть глаголом-связкой в именном сказуемом (в значении "есть").<br><br>

        В отличие от русского языка, в английском языке глагол-связка никогда не опускается, поскольку английское
        предложение имеет строго фиксированный порядок слов: подлежащее (subject) + сказуемое (verb) + дополнение
        (object)

        <br>
        <blockquote class="style1">
            <span>
            I am a doctor.<br>
            Я врач. (Я есть врач.)<br><br>

            The weather is bad.<br>
            Погода плохая.<br><br>

            They are from Paris.<br>
            Они из Парижа.</span>
        </blockquote>
        <br>
        Итак, глагол-связка to be в английском предложении никогда не опускается, т.к. он входит в именное сказуемое, и
        его место после подлежащего. На русский же язык глагол to be в данных случаях не переводится:<br>
        <blockquote class="style1">
            <span>
            I am happy.<br>
            Я счастлив<br><br>

            The book is interesting.<br>
            Книга интересная.<br><br>

            He is our teached.<br>
            Он наш учитель.</span>
        </blockquote>
        <br>
        Глагол to be не требует вспомогательного глагола для образования вопросительной или отрицательной формы.<br>

        Чтобы задать вопрос нужно поставить глагол to be перед подлежащим:
        <blockquote class="style1">
            <span>
            Am I happy?<br>
Is the book interesting?<br>
Is he our teacher?<br></span>
        </blockquote>
        <br>
        Для образования отрицательной формы достаточно поставить отрицательную частицу not после глагола to be:Для
        образования отрицательной формы достаточно поставить отрицательную частицу not после глагола to be:<br>
        <blockquote class="style1">
            <span>
            I am not happy.<br>
The book is not interesting.<br>
He is not our teacher.<br> </span>
        </blockquote>
        <br>
        В разговорной речи отрицательная частица not часто сливается с глаголом to be, образуя сокращения:<br>
        <blockquote class="style1">
            <span>
            is not = isn't<br>
are not = aren't<br> </span>
        </blockquote>
        <br>
        Также глагол to be может сокращаться, сливаясь с личным местоимением:<br>
        <blockquote class="style1">
            <span>
            I am = I'm<br>
We are = we're<br>
He is =  he's<br></span>
        </blockquote
