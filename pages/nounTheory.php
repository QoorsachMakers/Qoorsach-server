        Существительные в английском языке
    </h1>
    <article>
        <b>Имя существительное</b> – это знаменательная часть речи, обозначающая предмет в самом широком смысле.
        Например, существительные могут обозначать вещи (car, pen), живых существ (cat, human), места (Norway, mountain,
        Moscow), материалы (copper, cloth), процессы (life, laughter), состояния (rest, sleep), абстрактные понятия
        (beauty, evil) и качества (kindness, bravery).<br>
    </article>
    <h2>
        Строение английских существительных
    </h2>
    <article>
        По морфологическому составу существительные можно разделить на простые, производные и составные.<br>
        <br>
        <b>Простые существительные </b>(simple nouns) состоят из одного корня:<br>
        <blockquote>
            <span>
                gnome, rock, sky
            </span>
        </blockquote>
        <br>
        <b>Производные существительные </b>(derived nouns/derivatives) состоят из корня и одной или нескольких морфем
        (приставок или суффиксов):<br>
        <blockquote>
            <span>
                friendship, richness, teacher
            </span>
        </blockquote>
        <br>
        Для существительных характерны такие суффиксы, как -ant/-ent, -dom, -er/-or/-ar, -hood, -ion, -ity, -ment,
        -ness, -ship.<br>
        <b>Составные существительные </b>(compound nouns) состоят по крайней мере из двух корней:<br>
        <blockquote>
            <span>
                seaman, editor-in-chief
            </span>
        </blockquote>
        <br>
    </article>
    <h2>Классификация существительных в английском языке</h2>
    <article>
        Существительные делятся на имена собственные и нарицательные.<br>
        <br>
        <b>Имена собственные</b> (proper nouns) обозначают единственные в своем роде предметы или предметы, выделяемые
        из общего класса:<br>
        <ul>

            <li>
                географические названия, национальности и языки (<span>Africa, Poland, the Greeks, French</span>)
            </li>
            <li>
                личные имена и клички животных (<span>Peter, Smith, Lucky</span>)
            </li>
            <li>
                названия месяцев и дней недели (<span>Monday, August</span>)
            </li>
            <li>
                названия небесных тел (<span>Jupiter, the Sun</span>)
            </li>
            <li>
                названия кораблей, отелей, клубов (<span>«The Striker»</span>)
            </li>
            <li>
                названия праздников (<span>Halloween, the New Year</span>)
            </li>
            <li>
                названия зданий, улиц, парков, мостов (<span>Hyde Park, Broadway</span>)
            </li>
            <li>
                учреждения, организации, печатные издания (<span>North Atlantic Treaty Organization, the Times</span>)
            </li>
        </ul>
        Имена собственные пишутся с большой буквы (включая названия месяцев и дней недели, в отличие от русского языка).
        Если это сочетание нескольких слов, то все они, кроме артиклей и предлогов, тоже пишутся с большой буквы (the
        United States of America).<br>
        <br>
        <b>Имена нарицательные </b> (common nouns) – это общие названия для всех однородных предметов:<br>
        <blockquote>
                <span>
                    girl, table, town
                </span>
        </blockquote>
        <br>
        В свою очередь, имена нарицательные делятся на:<br>
        <ul>
            <li>
                <b>исчисляемые</b> (countable), которые могут быть посчитаны и имеют форму множественного числа:<br>
                <blockquote>
                        <span>
                            one chair, seven chairs
                        </span>
                </blockquote>
            </li>
            <li>
                <b>Неисчисляемые</b> (uncountable), которые не могут быть посчитаны и не имеют формы множественного
                числа:<br>
                <blockquote>
                        <span>
                            iron, communism
                        </span>
                </blockquote>
            </li>
            <li>
                <b>собирательные </b>(collective), - имеют форму единственного числа, но обозначают при этом группы лиц
                или понятий, рассматриваемые одно целое:<br>
                <blockquote>
                        <span>
                            team, money
                        </span>
                </blockquote>
            </li>
        </ul>
        Некоторые английские существительные в разных значениях могут быть как исчисляемыми, так и неисчисляемыми:<br>
        <blockquote>
                <span>
                    My dad reads a couple of <b>papers</b><br>
                    Мой папа читает пару газет<br>
                    <br>
                    <b>Paper </b>was invented in China<br>
                    Бумага была изобретена в китае<br>
                    <br>
                    Her <b>beauty</b> was stunning<br>
                    Её красота была сногсшибательной
                </span>
        </blockquote>
        <br>
        Исчисляемые делятся на:<br>
        <ul>
            <li>
                <b>Конкретные</b> (concrete) - названия отдельных предметов и живых существ:<br>
                <blockquote>
                        <span>
                            book, ticket, tree
                        </span>
                </blockquote>
            </li>
            <li>
                <b>абстрактные</b> или отвлеченные (abstract) - названия неисчисляемых понятий:<br>
                <blockquote>
                        <span>
                            Buddhism, progress
                        </span>
                </blockquote>
            </li>
            <li>
                <b>Вещественные</b> (material) - названия различных веществ и материалов:<br>
                <blockquote>
                        <span>
                            milk, aluminum
                        </span>
                </blockquote>
            </li>
        </ul>
        Впрочем, иногда английские существительные, обозначающие материалы, могут использоваться и во множественном
        числе. В таком случае они обозначают "сорт, вид" или "порция":<br>
        <blockquote>
                <span>
                        “Old Toby” is one of the most famous tobaccos in the world.<br>
                        «Старый Тоби» – это один из самых известных в мире сортов табака.<br>
                        <br>
                        Our plant produces cement and several asphalts.<br>
                        Наш завод производит цемент и несколько типов асфальта.<br>
                        <br>
                        Give me two coffees, please.<br>
                        Дайте мне два кофе, пожалуйста.<br>
                </span>
        </blockquote>
        Собирательные существительные делятся на:<br>
        <ul>
            <li>
                <b>Одушевленные</b> (animate) - названия живых существ (людей, животных), отвечают на вопрос "Кто?":<br>
                <blockquote>
                        <span>
                            team, family, army
                        </span>
                </blockquote>
            </li>
            <li>
                <b>Неодушевленные</b> (inanimate) - названия неживых предметов и растений, отвечают на вопрос
                "Что?":<br>
                <blockquote>
                        <span>
                            money, news, information
                        </span>
                </blockquote>
            </li>
        </ul>
