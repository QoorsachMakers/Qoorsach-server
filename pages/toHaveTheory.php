    <article>
        Значение этого глагола - "иметь, владеть, обладать". Часто в разговорной речи вместо have, has употребляется
        словосочетание have got, has got (краткие формы 've got и 's got) с тем же значением, особенно когда речь идёт о
        временном владении или только что приобретённом предмете или предметах:<br>
        <blockquote class="style1">
            <span>
                We've got a nice flat.<br>
                У нас хорошая квартира<br><br>
                Have you got any pets?<br>
                У вас есть домашние животные?
            </span>
        </blockquote>
        В вопросительной форме в британском варианте языка глагол to have часто стоит перед подлежащим, в американском
        варианте вопросительная и отрицательная формы всегда образуются с помощью вспомогательного глагола do:<br>
        <blockquote class="style1">
            <span>
                Have you two sons? (британский)<br>
                Do you have a lot of free time? (американский)
            </span>
        </blockquote>
        Отрицание выражается формой глагола to have с отрицанием not или с отрицательным местоимением no:<br>
        <blockquote class="style1">
            <span>
                I haven't got a pen.<br>
                I have got no pen here<br><br>
                has not = hasn't<br>
                have not = haven't
            </span>
        </blockquote>
    </article>
