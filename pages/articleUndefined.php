    <h1>Неопределенный артикль в английском языке</h1>
    <article>
        Неопределенный артикль (a) произошел от числительного one и используется, как правило, с существительными в
        единственном числе, обозначающими исчисляемые предметы.<br>
        <br>
        Неопределенный артикль в английском языке может иметь три значения: классифицирующее, обобщающее и
        численное.<br>
        <br>
        Артикль в классифицирующем (причисляющем) значении относит предмет к определенному классу или группе
        предметов:<br>
        <blockquote>
            <span>
                There is a ball on the grass.<br>
                На траве лежит мяч. (не волан, не слон, не человек)<br>
                <br>
                It is a golf ball.<br>
                Это – мяч для гольфа. (не для футбола или тенниса)<br>
                <br>
                I smell an omelette.<br>
                Я чувствую запах омлета. (не супа или каши)<br>
            </span>
        </blockquote>
        <br>
        То есть существительное с классифицирующим артиклем причисляет предмет к определенном классу, но не ссылается на
        него прямо.<br>
        <br>
        К этому значению также относится использование неопределенного артикля в восклицательных предложениях со словом
        what:<br>
        <blockquote>
            <span>
                What a tall man!<br>
                Какой высокий человек!
            </span>
        </blockquote>
        <br>
        и перед исчисляемыми существительными в единственном числе со словами rather, quite, such и most (очень):<br>
        <blockquote>
            <span>
                It’s quite a difficult question.<br>
Это довольно трудный вопрос.
            </span>
        </blockquote>
        <br>
        Артикль в обобщающем значении означает, что существительное является представителем некоего класса, и всё, что о
        нем сказано в предложении, можно отнести и к другим предметам этого класса:<br>
        <blockquote>
            <span>
                A limerick is a short comic verse consisting of five lines.<br>
                Лимерик – это короткий юмористический стишок из пяти строк.<br>
                <br>
                A kitten is a young cat.<br>
                Котенок – это молодая кошка.
            </span>
        </blockquote>
        <br>
        В отличие от классифицирующих артиклей, которые вводят новое понятие или информацию, то есть самую важную часть
        предложения, обобщающие артикли лишь открывают собой высказывание.<br>
        <br>
        В численном значении неопределенные артикли выражают свое исходное значение числа «один»:<br>
        <blockquote>
            <span>
                We could stay in Prague for only a day.<br>
                Мы могли оставаться в Праге только один день.<br>
                <br>
                This task will take me an hour.<br>
                Эта задача займет у меня (один) час.
            </span>
        </blockquote>
        <br>
        Также артикли в численном значении могут приобретать значение «рюмка», «порция», «разновидность» и т. п.:<br>
        <blockquote>
            <span>
                I’d like a whiskey.<br>
                Я бы хотел (одну) рюмку виски.<br>
                <br>
                Let’s drink a coffee or two?<br>
                Может, выпьем одну-две чашечки кофе?<br>
                <br>
                a very expensive wine<br>
                очень дорогой сорт вина
            </span>
        </blockquote>
        <br>
