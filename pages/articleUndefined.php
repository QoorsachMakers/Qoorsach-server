    <h2>
        Неопределенный артикль во множественном числе
    </h2>
    <article>
        Английские существительные, которые в единственном числе используются с неопределенным артиклем, во
        множественном числе артикля не имеют:<br>
        <blockquote>
            <span>
                This is a Chinese character.<br>
                Это – китайский иероглиф.<br>
                <br>
                Those are Chinese characters.<br>
                Это – китайские иероглифы.<br>
            </span>
        </blockquote>
        <br>
        Если артикль имеет численное значение, то во множественном числе его можно заменить на неопределенные
        местоимения some (в утвердительных предложениях) и any (в вопросительных и отрицательных предложениях). В
        зависимости от контекста их тогда можно перевести как «несколько», «какие-нибудь», «никакие»:<br>
        <blockquote>
            <span>
                We saw some temples on the excursion.<br>
                На экскурсии мы увидели несколько храмов.<br>
                <br>
                Did you see any temples on the excursion?<br>
                На экскурсии вы видели какие-нибудь храмы?<br>
                <br>
                We did not see any temples on the excursion.<br>
                На экскурсии мы не видели никаких храмов.<br>
            </span>
        </blockquote>
        <br>
        Также на количество вместо артикля могут указывать слова many, few (мало), a few (немного, несколько) или
        количественные числительные:<br>
        <blockquote>
            <span>
                She broke a vase yesterday.<br>
                Вчера она разбила вазу.<br>
                <br>
                She broke two vases yesterday.<br>
                Вчера она разбила две вазы.<br>
                <br>
                She broke a few vases yesterday.<br>
                Вчера она разбила несколько ваз.<br>
            </span>
        </blockquote>
        <br>
    </article>
    <h2>
        Неопределенный артикль с именами собственными
    </h2>
    <article>
        Неопределенный артикль употребляется со следующими именами собственными:
        <ul>
            <li>
                Отдельные члены семьи:<br>
                <blockquote>
                    <span>
                        She is a Willis, and this means a lot.<br>
                        Она – Уиллис (одна из семьи Уиллис), а это многое значит.
                    </span>
                </blockquote>
            </li>
            <li>
                Неизвестные люди:<br>
                <blockquote>
                    <span>
                        There is a Mrs. Limms waiting for you.<br>
                        Вас ждет некая миссис Лиммс.
                    </span>
                </blockquote>
            </li>
            <li>
                Имена собственные, употребляющиеся как нарицательные:<br>
                <blockquote>
                    <span>
                        I’m not a Rambo, am I?<br>
                        Что я, по-твоему, какой-то Рембо?<br>
                        <br>
                        I do not have imagination of a Lewis Carroll.<br>
                        У меня нет воображения, как у Льюиса Кэрролла (кэрролловского воображения).<br>
                    </span>
                </blockquote>
            </li>
            <li>
                Когда имеется в виду состояние или черта существа или места:<br>
                <blockquote>
                    <span>
                        When we arrived, we saw a disturbed London: the war had started.<br>
                        Когда мы прибыли, то увидели растревоженный Лондон: началась война.<br>
                        <br>
                        Her daughter resembles her a lot: she looks exactly like a young Mary.<br>
                        Ее дочь очень на нее похожа: она выглядит точно, как Мери в юности.
                    </span>
                </blockquote>
                <br>
            </li>
        </ul>
    </article>
