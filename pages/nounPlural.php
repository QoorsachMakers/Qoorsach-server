    <h1>
        Множественное число существительных в английском языке
    </h1>
    <article>
        Существительные в английском языке, как и в русском, могут стоять в форме единственного или множественного
        числа.<br>
        <b> Единственное число</b> (singular) обозначает один предмет:<br>
        <blockquote>
            <span>
                cup, gun, bubble
            </span>
        </blockquote>
        <br>
        <b>Множественное число</b> (plural) обозначает два и более предмета:<br>
        <blockquote>
            <span>
                three cups, two guns, thousands of bubbles
            </span>
        </blockquote>
        <br>
    </article>
    <h2>
        Образование множественного числа английских существительных
    </h2>
    <article>
        Английское существительное можно поставить во множественное число, прибавив к нему окончание -s. Оно читается
        как [ z ] после гласных и звонких согласных:<br>
        <blockquote>
            <span>
                shoe - shoe<b>s</b><br>
                hen - hen<b>s</b>
            </span>
        </blockquote>
        <br>
        или как [ s ] после глухих согласных:<br>
        <blockquote>
            <span>
                bat - bat<b>s</b>
            </span>
        </blockquote>
        <br>
        Если существительное оканчивается на свистящий или шипящий звук, то есть на буквы s, ss, x, sh, ch, то для него
        форма множественного числа образуется при помощи окончания -es [ iz ]:<br>
        <blockquote>
            <span>
                bass - bass<b>es</b><br>
                match - match<b>es</b><br>
                leash - leash<b>es</b><br>
                box - box<b>es</b>
            </span>
        </blockquote>
        <br>
        Если существительное оканчивается на букву -y, перед которой стоит согласная, то во множественном числе -y
        меняется на i и к слову прибавляется окончание -es:<br>
        <blockquote>
            <span>
                lobb<b>y</b> - lobb<b>ies</b><br>
                sk<b>y</b> - sk<b>ies</b>
            </span>
        </blockquote>
        <br>
        Исключения: имена собственные (the two Germanys, the Gatsbys) и составные существительные (stand-bys).<br>
        <br>
        Если перед буквой -y стоит гласная, то множественное число образуется по общему правилу при помощи окончания -s,
        а буква y остается без изменений:<br>
        <blockquote>
            <span>
                bay - bay<b>s</b><br>
                day - day<b>s</b><br>
                way - way<b>s</b><br>
            </span>
        </blockquote>
        <br>
        К существительным заканчивающимся на -o прибавляется окончание -es:<br>
        <blockquote>
            <span>
                potat<b>o</b> - potato<b>es</b><br>
                tomat<b>o</b> - tomato<b>es</b><br>
                her<b>o</b> - hero<b>es</b>
            </span>
        </blockquote>
        <br>
        Исключения: bamboos, embryos, folios, kangaroos, radios, studios, zoos, Eskimos, Filipines, kilos, photos, pros,
        pianos, concertos, dynamos, solos, tangos, tobaccos.<br>
        <br></article>
    <h2>
        Исключения при образовании множественного числа английских существительных
    </h2>
    <article>
        Некоторые существительные по историческим причинам имеют свои собственные способы построения множественного
        числа:<br>
        <blockquote>
            <span>
                man - men<br>
                woman - women<br>
                tooth - teeth<br>
                foot - feet<br>
                goose - geese<br>
                mouse - mice<br>
                louse - lice<br>
                child - children<br>
                ox - oxen<br>
                brother - brethren
            </span>
        </blockquote>
        <br>
        Для некоторых слов форма единственного числа совпадает с формой множественного:<br>
        <blockquote>
            <span>
                sheep - sheep<br>
                swine - swine<br>
                deer - deer<br>
                grouse - grouse<br>
                series - series<br>
                species - species<br>
                corps - corps
            </span>
        </blockquote>
        <br>
        Также это характерно для названий некоторых национальностей – Chinese, Japanese, Portuguese, Swiss:<br>
        <blockquote>
            <span>
                a <b>Chinese</b> - a group of seven <b>Chinese</b><br>
                (один) китаец - группа из семи китайцев
            </span>
        </blockquote>
        <br>
        Довольно большая часть заимствованных слов сохранила за собой окончания множественного числа, образованные по
        правилам «своего» языка (как правило, греческого и латинского):<br>
        <blockquote>
            basis - bases<br>
            crisis - crises<br>
            phenomenon - phenomena<br>
            stimulus - stimuli<br>
            formula - formulae<br>
            datum - data<br>
            index - indices<br>
            bureau - bureaux<br>
        </blockquote>
        <br>
        Для некоторых из таких заимствований допустимо образование множественного числа по правилам английской
        грамматики:<br>
        <blockquote>
            <span>
                formula - formulas / formulae<br>
                focus - focuses / foci
            </span>
        </blockquote>
        <br>
        Обычно английская форма используется в повседневной речи и художественной литературе, а исходная – в научных
        трудах.<br>

    </article>
    <h2>
        Множественное число в составных существительных
    </h2>
    <article>
        В составных существительных форму множественного числа обычно принимает только второй элемент:<br>
        <blockquote>
                <span>
                    housewi<b>ves</b>, schoolchild<b>ren</b>
                </span>
        </blockquote>
        <br>
        В составных существительных с первым элементом man / woman во множественном числе изменяются обе части:<br>
        <blockquote>
            <span>wom<b>e</b>n-writer<b>s</b>, gentlem<b>e</b>n-farmer<b>s</b><br>
            писательницы, фермеры-джентльмены</span>
        </blockquote>
        <br>
        В словах с составляющей -man она изменяется на -men:<br>
        <blockquote>
            <span>
                policem<b>a</b>n - policem<b>e</b>n<br>
            </span>
        </blockquote>
        <br>
        Если части составного слова пишутся через дефис, то в форму множественного числа ставится ключевой по смыслу
        компонент:<br>
        <blockquote>
            <b>man</b>-of-war – <b>men</b>-of-war<br>
            <b>mother</b>-in-law – <b>mothers</b>-in-law<br>
            hotel-<b>keeper</b> – hotel-<b>keepers</b><br>
            gas-<b>mask</b> – gas-<b>masks</b><br>
        </blockquote>
        <br>
        Если в составном слове нет элемента-существительного, то для образования множественного числа нужно прибавить -s
        к последнему элементу:<br>
        <blockquote>
            <span>forget-me-nots, drop-outs, go-betweens</span>
        </blockquote>
        <br>
    </article>
    <h2>
        Существительные, используемые только в единственном числе
    </h2>
    <article>
        Некоторые английские существительные, например, неисчисляемые, используются только в форме единственного
        числа:<br>
        <blockquote>
            <span>
                gold, silver, oil, music, the Thames
            </span>
        </blockquote>
        <br>
        В этом они могут отличаться от русского языка, где могут иметь форму множественного числа:<br>
        <blockquote>
            <span>
                 advice (совет/советы), information (информация/сведения), progress (успех/успехи), knowledge (знание/знания)
            </span>
        </blockquote>
        <br>
        Такие слова, как dozen (дюжина), score (десяток), couple, pair (пара), stone (мера веса стоун), head (голова
        скота) имеют обе формы числа, но, если они употребляются вместе с конкретным числительным, то остаются в
        единственном числе:<br>
        <blockquote>
            <span>four <b>dozen</b> eggs, <b>two score</b> tables</span>
            <span> четыре дюжины яиц, два десятка столов</span>
        </blockquote>
        Если же они использованы в значении «много», то принимают форму множественного числа:<br>
        <blockquote>
            <span>
                <b>scores</b> of people, <b>dozens</b> of boxes<br>
                 десятки людей, дюжины коробок
            </span>
        </blockquote>
        <br>
        Слово news, works (завод), barracks и названия некоторых наук на -ics имеют форму множественного числа, но
        используются в значении единственного:<br>
        <blockquote>
            <span>
                This <b>news is</b> horrible<br>
                Ужасная новость<br>
                <br>
                <b>Physics</b> stud<b>ies</b> matter and motion<br>
                Физика изучает материю и движение
            </span>
        </blockquote>
        <br>
        Слова money и hair (кроме значения «волосок») в английском языке, в отличие от русского, употребляются только в
        единственном числе:<br>
        <blockquote>
            <span>
                her soft <b>hair</b>, this <b>money</b><br>
                её мягкие волосы, эти деньги
            </span>
        </blockquote>
        <br>
    </article>
    <h2>
        Существительные, используемые только во множественном числе
    </h2>
    <article>
        Как и в русском языке, к этой категории относятся многие парные предметы (scissors, scales, spectacles,
        trousers), некоторые географические названия (the Netherlands, the Highlands, the East Indies) и другие слова
        (customs, manners, outskirts, annals).<br>
        <br>
        В нее входят некоторые слова, которые в русском языке имеют форму единственного числа: goods (товар/товары),
        contents (содержание), clothes (одежда), proceeds (доход/доходы), wages (зарплата), riches (богатство/богатства)
        и др.<br>
        <br>
        Существительное people в значении «люди» имеет значение множественного лица:<br>
        <blockquote>
            <span>
                <b>People</b> are so mean here<br>
                Люди здесь такие злые
            </span>
        </blockquote>
        <br>
        Однако в значении «народ» оно может использоваться и в единственном, и в множественном числе.<br>
        <blockquote>
            UNO helps all <b>peoples</b> of the world<br>
            ООН помогает всем народам мира
        </blockquote>
    </article>
