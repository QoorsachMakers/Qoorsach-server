    <h1>Определенный артикль в английском языке</h1>
    <article>
        <b>Определенный артикль (the)</b> сопровождает те существительные, которые уже должны быть известны слушателю из
        контекста, ситуации или из его общих знаний. Также он часто указывает на уникальные, единственные в своем роде
        предметы.<br>
        <br>
        Определенный артикль в английском языке обладает двумя значениями: конкретизирующим и обобщающим.<br>
        <br>
        <b>Артикль в конкретизирующем значении </b>выделяет конкретный, отдельный предмет из объектов одного типа. Это
        выделение может осуществляться при помощи определения, контекста, ситуации или значения самого существительного:<br>
        <br>
        <blockquote>
            <span>
                The higher tree in front of us is called oak.<br>
                Более высокое дерево впереди нас называется дубом.<br>
                <br>
                William wanted to continue his story, but suddenly his wife called him upstairs. Relieved, we were enjoying the silence.<br>
                Вильям хотел продолжить свою историю, но вдруг жена позвала его наверх. Мы с облегчением наслаждались (этой) тишиной.<br>
                <br>
                Answer the phone.<br>
                Ответь на звонок. – хотя предмет упоминается впервые, используется определенный артикль, так как из ситуации очевидно, какой телефонный аппарат имеется в виду.<br>
            </span>
        </blockquote>
        <br>
        Такие определения, как next, following, last, very и only, а также порядковые числительные конкретизируют
        существительное, поэтому оно должно использоваться с определенным артиклем:<br>
        <blockquote>
            <span>
                The only question I have is how much I will be paid.<br>
                У меня есть только один вопрос – сколько мне заплатят.<br>
                <br>
                The second attempt turned out more successful.<br>
                Вторая попытка оказалась более удачной.<br>
            </span>
        </blockquote>
        <br>
        Кроме этого, определенный артикль используется с прилагательными в превосходной степени:<br>
        <blockquote>
            <span>
                the tallest tower in the world<br>
                самая высокая башня в мире
            </span>
        </blockquote>
        <br>
        <i>Примечание:</i><br>
        Иногда английские прилагательные в превосходной степени могут использоваться и с неопределенным артиклем, тогда
        оно приобретает значение «весьма, крайне»:<br>
        <blockquote>
            <span>
                My neighbour is a most extraordinary man.<br>
                Мой сосед – весьма своеобразный человек.
            </span>
        </blockquote>
        <br>
        <b>Артикль в обобщающем (родовом) значении </b>относит предмет к целому классу предметов одного вида.<br>
        <blockquote>
            <span>
                The Bengal tiger is on the brink of extinction. (не один конкретный тигр, а весь вид)<br>
                Бенгальский тигр находится на грани исчезновения.<br>
                <br>
                The atom is the smallest unit of substance.<br>
                Атом – это мельчайшая единица вещества.<br>
            </span>
        </blockquote>
        <br>
        Если артикль имеет такое значение, то во множественном числе он опускается:<br>
        <blockquote>
            <span>
                Bengal tigers are magnificent animals.<br>
                Бенгальские тигры – это великолепные животные.<br>
            </span>
        </blockquote>
        <br>
        Артикли в обобщающем значении также используются с субстантивированными прилагательными (то есть перешедшими в
        класс существительных) – the sick, the poor, the strong – и названиями социальных классов и различных
        направлений – the workers, the aristocracy, the modernists.<br>
        <br>
        Также он может указывать на:
        <ul>
            <li>
                различные системы и составляющие инфраструктуры: When the underground starts to work? The phone is so
                expensive here.
            </li>
            <li>
                виды искусств: The theater was very popular two centuries ago.
            </li>
            <li>
                названия больших отрезков времени и эпох: mysteries of the past, wars of the Middle Ages
            </li>
            <li>
                некоторые названия мест: at the chemist's
            </li>
            <li>
                части тела: pains in the back
            </li>
            <li>
                неисчисляемые вещественные существительные, если имеется в виду конкретное количество вещества:
                <blockquote>
                    <span>
                        The milk that I bought in your shop was spoiled.<br>
                        Молоко, которое я купил в вашем магазине, было просрочено.<br>
                    </span>
                </blockquote>
            </li>
            <li>
                сочетания с некоторыми существительными: the press, the government, the army и др.
            </li>
        </ul>
    </article>
    <h2>
        Определенный артикль во множественном числе
    </h2>
    <article>
        Английские существительные, которые в единственном числе используются с определенным артиклем, сохраняют его и
        во множественном числе:<br>
        <blockquote>
            <span>
                He liked the present I bought.<br>
                Ему понравился купленный мною подарок.<br>
                <br>
                He liked the presents I bought.<br>
                Ему понравились купленные мною подарки.<br>
            </span>
        </blockquote>
        <br>
        Определенный артикль используется с существительными во множественном числе также, если имеются в виду все
        представители этой группы:<br>
        <blockquote>
            <span>
                The city-dwellers often suffer air pollution.<br>
                Жители городов (все жители города) часто страдают от загрязнения воздуха.<br>
                <br>
                This candidate is not very popular among city-dwellers.<br>
                Этот кандидат не очень популярен среди жителей городов (не всех, а только некоторых).<br>
            </span>
        </blockquote>
        <br>
        Кроме этого, определенный артикль может использоваться с сочетаниями существительных и количественных
        числительных, если они уже известны читателю:<br>
        <blockquote>
            <span>
                Steven and Mark dislike each other, the two have a fight almost every day.<br>
                Стивен и Марк недолюбливают друг друга, эти двое дерутся почти каждый день.
            </span>
        </blockquote>
        <br>
    </article>
